Mob.__index = Mob

local ENEMY_PARENT = "Enemies"
local HUMAN_PROP   = "Humanoid"
local ROOT_PROP    = "HumanoidRootPart"

function Mob.new(services)
    local self = setmetatable({}, Mob)
    self._services = services
    return self
