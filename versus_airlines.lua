-- ============================================================================
-- Boot
-- ============================================================================
local function boot()
    -- 1. Mount the Versus window.
    local Library = Window.create()
    if not Library then
        warn("[Versus-Airlines] window mount failed; aborting boot")
        return nil
    end

    -- 2. Boot the kernel graph.
    local kernel = Kernel.boot(Library)
    if not kernel then
        warn("[Versus-Airlines] kernel boot failed; aborting")
        return nil
    end

    -- 3. Register all 11 toggle modules.
    kernel.registry:register(mod_autofarm)
    kernel.registry:register(mod_bring)
    kernel.registry:register(mod_boss)
    kernel.registry:register(mod_fruit)
    kernel.registry:register(mod_sea_event)
    kernel.registry:register(mod_raid)
    kernel.registry:register(mod_v4)
    kernel.registry:register(mod_esp)
    kernel.registry:register(mod_teleport)
    kernel.registry:register(mod_party)
    kernel.registry:register(mod_progression)

    -- 4. Mount the section tree.
    Sections.build(Library, kernel)

    -- 5. Start the live status label.
    Status.start(kernel)

    -- 6. Start the watchdog.
    if kernel.watchdog and kernel.watchdog.start then
        kernel.watchdog:start()
    end

    -- 7. Eager-warm the services cache.
    if kernel.services and kernel.services.init then
        kernel.services:init()
    end

    -- 8. Probe the current game.
    if kernel.game and kernel.game.detectSea then
        local sea = kernel.game:detectSea()
        if sea == 0 then
            kernel.events:emit("milestone", { text = "not in Blox Fruits (or unknown placeId)", level = "warn" })
        end
    end

    -- 9. Load the data tables (now in-file as Data_* locals).
    if kernel.data and kernel.data.loadAll then
        kernel.data:loadAll()
    end

    -- 10. Boot complete.
    return kernel
end

-- ============================================================================
-- Inlined data tables: register each with the kernel's data registry
-- at load time. This is a no-op if the registry's loadAll uses
-- require() (it does, on disk) but in single-file mode we need to
-- inject the tables manually.
-- ============================================================================
do
    -- After Kernel.boot, kernel.data is built. We register our
    -- in-memory tables now so the registry's :get() finds them.
    if _G.VersusKernel and _G.VersusKernel.data then
        local d = _G.VersusKernel.data
        d._tables = d._tables or {}
        d._tables.sea1_quests    = Data_sea1_quests
        d._tables.sea2_quests    = Data_sea2_quests
        d._tables.sea3_quests    = Data_sea3_quests
        d._tables.bosses         = Data_bosses
        d._tables.swords         = Data_swords
        d._tables.fighting_styles= Data_fighting_styles
        d._tables.fruits         = Data_fruits
        d._tables.accessories    = Data_accessories
        d._tables.guns           = Data_guns
        d._tables.materials      = Data_materials
        d._tables.raids          = Data_raids
        d._tables.race_v4        = Data_race_v4
        d._tables.sea_events     = Data_sea_events
        d._tables.islands        = Data_islands
        d._tables.quest_npcs     = Data_quest_npcs
        d._tables.fruit_dealers  = Data_fruit_dealers
        d._tables.enemy_spawn_db = Data_enemy_spawn_db
    end
end

-- Auto-run on load. The user can also call boot() themselves.
local _kernel = boot()
return _kernel
